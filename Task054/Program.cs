// Задача 54: Задайте двумерный массив. Напишите программу, 
// которая упорядочит по убыванию элементы каждой строки двумерного массива.
// Например, задан массив:
// 1 4 7 2
// 5 9 2 3
// 8 4 2 4
// В итоге получается вот такой массив:
// 7 4 2 1
// 9 5 3 2
// 8 4 4 2
//===============================
using System; // Подключение пространства имен System
 class Program
{
    static void Main() // Основной метод программы
    {
        int[,] array = { // Объявление и инициализация двумерного массива
            { 1, 4, 7, 2 },
            { 5, 9, 2, 3 },
            { 8, 4, 2, 4 }
        };
         SortRowsDescending(array); // Вызов функции для сортировки строк массива по убыванию
         Console.WriteLine("Отсортированный массив:"); // Вывод строки "Отсортированный массив:"
        PrintArray(array); // Вызов функции для вывода массива на консоль
    }
     static void SortRowsDescending(int[,] array) // Функция для сортировки строк массива по убыванию
    {
        int rows = array.GetLength(0); // Получение количества строк массива
        int columns = array.GetLength(1); // Получение количества столбцов массива
         for (int i = 0; i < rows; i++) // Цикл по строкам массива
        {
            for (int j = 0; j < columns - 1; j++) // Цикл по элементам строки
            {
                for (int k = 0; k < columns - j - 1; k++) // Цикл для сравнения и обмена элементов
                {
                    if (array[i, k] < array[i, k + 1]) // Проверка, если текущий элемент меньше следующего
                    {
                        int temp = array[i, k]; // Временная переменная для обмена значениями
                        array[i, k] = array[i, k + 1]; // Обмен значениями
                        array[i, k + 1] = temp; // Присвоение временной переменной
                    }
                }
            }
        }
    }
     static void PrintArray(int[,] array) // Функция для вывода массива на консоль
    {
        int rows = array.GetLength(0); // Получение количества строк массива
        int columns = array.GetLength(1); // Получение количества столбцов массива
         for (int i = 0; i < rows; i++) // Цикл по строкам массива
        {
            for (int j = 0; j < columns; j++) // Цикл по элементам строки
            {
                Console.Write(array[i, j] + " "); // Вывод элемента массива с пробелом
            }
            Console.WriteLine(); // Переход на новую строку
        }
    }
}
